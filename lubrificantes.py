class Graxa:
	def __init__(self, idGra, tipo, consis):
		self.idGra = idGra
		self.tipo = tipo
		self.consis = consis	
		

	def getIdGra(self):
		return self.idGra
	def setIdGra(self, idGra):
		self.idGra = idGra

	def getTipo(self):
		return self.tipo
	def setTipo(self, tipo):
		self.tipo = tipo

	def getConsis(self):
		return self.consis
	def setConsis(self, consis):
		self.consis = consis

